"""Модуль 2 — Перші слова й речення. Найпростіша граматика, щоб одразу говорити."""

from __future__ import annotations

from app.grammar.schema import Card, Lesson, Module, Quiz

MODULE = Module(
    id="podstawy",
    icon="🧱",
    title="Перші слова й речення",
    subtitle="Мінімум граматики, щоб уже будувати прості речення.",
    lessons=[
        # ── Урок 1: To jest ──
        Lesson(
            id="pod_tojest",
            title="«To jest…» — це є…",
            cards=[
                Card(
                    "Найпростіше речення",
                    "<b>To jest…</b> = «Це (є)…». Універсальний спосіб назвати будь-що. "
                    "У польській, на відміну від української, <b>«є» не пропускають</b>.",
                    [("To jest dom.", "Це дім."), ("To jest kot.", "Це кіт.")],
                ),
                Card(
                    "Питання й заперечення",
                    "Питання: <b>Co to jest?</b> (Що це?). Заперечення: додай <b>nie</b>.",
                    [("Co to jest?", "Що це?"), ("To nie jest pies.", "Це не пес.")],
                ),
                Card(
                    "Хто це?",
                    "Про людей питаємо <b>Kto to jest?</b> (Хто це?).",
                    [("Kto to jest?", "Хто це?"), ("To jest Anna.", "Це Анна.")],
                ),
            ],
            quiz=[
                Quiz("Як сказати «Це дім»?", ["To dom.", "To jest dom.", "Dom to."], 1,
                     "Польська не пропускає «jest»: To jest dom."),
                Quiz("«Що це?» польською —", ["Kto to jest?", "Co to jest?", "Gdzie dom?"], 1,
                     "Co = що. Co to jest?"),
            ],
        ),
        # ── Урок 2: рід ──
        Lesson(
            id="pod_rodzaj",
            title="Рід іменника: ten / ta / to",
            cards=[
                Card(
                    "Три роди",
                    "Кожен іменник має рід: <b>чоловічий</b>, <b>жіночий</b> або <b>середній</b>. "
                    "Рід визначає, як слово змінюється далі, тож це важливо з самого початку.",
                ),
                Card(
                    "Як упізнати рід",
                    "Дивись на <b>закінчення</b>:\n• приголосний → <b>чоловічий</b> (ten)\n"
                    "• <b>-a</b> → <b>жіночий</b> (ta)\n• <b>-o, -e</b> → <b>середній</b> (to)",
                    [("ten dom", "цей дім (ч.р.)"), ("ta kobieta", "ця жінка (ж.р.)"),
                     ("to okno", "це вікно (с.р.)")],
                ),
                Card(
                    "Навіщо це знати",
                    "Рід підказує форму прикметників, займенників і дієслів у минулому часі. "
                    "Тому корисно вчити нове слово <b>разом із родом</b> (ten/ta/to).",
                    tip="Виняток: деякі чоловічі слова на -a (mężczyzna — чоловік). Їх мало, запамʼятаєш.",
                ),
            ],
            quiz=[
                Quiz("Якого роду слово «kobieta»?", ["чоловічого", "жіночого", "середнього"], 1,
                     "Закінчення -a → жіночий рід: ta kobieta."),
                Quiz("«okno» (вікно) — це рід…", ["чоловічий (ten)", "жіночий (ta)", "середній (to)"], 2,
                     "Закінчення -o → середній: to okno."),
            ],
        ),
        # ── Урок 3: займенники ──
        Lesson(
            id="pod_zaimki",
            title="Особові займенники: ja, ty, on…",
            cards=[
                Card(
                    "Хто діє",
                    "Займенники «я, ти, він…»:\n• <b>ja</b> — я\n• <b>ty</b> — ти\n"
                    "• <b>on / ona / ono</b> — він / вона / воно",
                    [("ja", "я"), ("ty", "ти"), ("ona", "вона")],
                ),
                Card(
                    "Множина",
                    "• <b>my</b> — ми\n• <b>wy</b> — ви\n• <b>oni / one</b> — вони",
                    [("my", "ми"), ("wy", "ви"), ("oni", "вони (з чоловіками)"),
                     ("one", "вони (без чоловіків)")],
                    tip="oni — якщо в групі є хоч один чоловік; one — жінки/речі/діти.",
                ),
                Card(
                    "Їх часто пропускають",
                    "У польській займенник-підмет часто <b>опускають</b>, бо його видно із дієслова "
                    "(як в українській). «Jestem» вже означає «я є».",
                    [("(Ja) jestem tu.", "Я тут."), ("(Ty) masz kota.", "Ти маєш кота.")],
                ),
            ],
            quiz=[
                Quiz("«Вони» (група жінок) —", ["oni", "one", "wy"], 1,
                     "Без чоловіків → one."),
            ],
        ),
        # ── Урок 4: być ──
        Lesson(
            id="pod_byc",
            title="Дієслово «być» (бути)",
            cards=[
                Card(
                    "Найважливіше дієслово",
                    "<b>być</b> = «бути». Його треба знати напамʼять — воно всюди.",
                ),
                Card(
                    "Однина",
                    "• (ja) <b>jestem</b> — я є\n• (ty) <b>jesteś</b> — ти є\n"
                    "• (on/ona) <b>jest</b> — він/вона є",
                    [("Jestem studentem.", "Я студент."), ("Jesteś miły.", "Ти милий."),
                     ("Ona jest w domu.", "Вона вдома.")],
                ),
                Card(
                    "Множина",
                    "• (my) <b>jesteśmy</b> — ми є\n• (wy) <b>jesteście</b> — ви є\n"
                    "• (oni/one) <b>są</b> — вони є",
                    [("Jesteśmy z Ukrainy.", "Ми з України."), ("Oni są tu.", "Вони тут.")],
                    tip="Запамʼятай трійку: jestem / jesteś / jest — і решта піде легко.",
                ),
            ],
            quiz=[
                Quiz("«Я є» польською —", ["jest", "jestem", "jesteś"], 1, "ja jestem."),
                Quiz("«Вони є» —", ["są", "jesteśmy", "jest"], 0, "oni/one są."),
            ],
        ),
        # ── Урок 5: питання й заперечення ──
        Lesson(
            id="pod_pytania",
            title="Питання та заперечення",
            cards=[
                Card(
                    "Питальні слова",
                    "<b>Kto?</b> — хто · <b>Co?</b> — що · <b>Gdzie?</b> — де · "
                    "<b>Jak?</b> — як · <b>Dlaczego?</b> — чому.",
                    [("Gdzie jesteś?", "Де ти?"), ("Jak masz na imię?", "Як тебе звати?")],
                ),
                Card(
                    "Питання «так/ні» — czy",
                    "Щоб спитати без питального слова, став на початку <b>czy</b> (наче «чи»).",
                    [("Czy masz czas?", "Чи маєш час?"), ("Czy to jest twój dom?", "Це твій дім?")],
                ),
                Card(
                    "Заперечення — nie",
                    "Просто постав <b>nie</b> перед дієсловом. Подвійне заперечення — це нормально "
                    "(як в українській).",
                    [("Nie wiem.", "Не знаю."), ("Nikt nie wie.", "Ніхто не знає.")],
                ),
            ],
            quiz=[
                Quiz("Як почати питання «так/ні»?", ["Co", "Czy", "Gdzie"], 1,
                     "Czy masz czas? — Чи маєш час?"),
                Quiz("«Не знаю» —", ["Nie wiem.", "Wiem nie.", "Nic wiem."], 0, "nie перед дієсловом."),
            ],
        ),
    ],
)
